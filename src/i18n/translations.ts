export const translate = (
  language: LanguageCode,
  key: TranslationKey,
  values?: Record<string, string>
) => {
  const template = translations[language][key] ?? english[key];

  if (!values) {
    return template;
  }

  return Object.entries(values).reduce(
    (text, [name, value]) => text.replace(`{${name}}`, value),
    template
  );
};
